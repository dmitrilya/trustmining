                    <div>
                        <h3 class="text-lg lg:text-xl font-semibold text-slate-800 dark:text-slate-200 mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">📊</span> Понятие остаточной стоимости на ОСНО (ст. 268 НК РФ)
                        </h3>
                        <p class="mb-2 text-slate-600 dark:text-slate-400">
                            В процессе эксплуатации майнингового оборудования на ОСНО его балансовая цена непрерывно уменьшается за счет ежемесячного начисления
                            амортизации. Когда возникает необходимость продать б/у АСИКи или видеокарты, налогообложение операции регулируется <strong
                                class="text-slate-800 dark:text-slate-200">статьей 268 НК РФ</strong>.
                        </p>
                        <p class="mb-4 sm:mb-5 lg:mb-6 text-slate-600 dark:text-slate-400">
                            Ключевым экономическим показателем при расчете налога является <strong class="text-slate-800 dark:text-slate-200">остаточная
                                стоимость</strong> амортизируемого имущества. Она рассчитывается по следующей строгой формуле:
                        </p>
                        <div
                            class="bg-white/40 dark:bg-slate-900/40 border border-slate-300 dark:border-slate-700 rounded-xl p-2 sm:p-4 shadow-md shadow-logo-color mb-4">
                            <p class="font-mono text-xs md:text-sm text-center text-slate-800 dark:text-slate-200 font-bold">Остаточная Стоимость =
                                Первоначальная Цена (Покупка) − Сумма Всей Начисленной Амортизации</p>
                        </div>
                        <p class="text-slate-600 dark:text-slate-400">
                            Согласно подпункту 1 пункта 1 статьи 268 НК РФ, при продаже оборудования организация (или ИП на ОСНО) имеет полное законное право
                            уменьшить полученный доход от реализации на величину этой остаточной стоимости.
                        </p>
                    </div>

                    <div>
                        <h3 class="text-lg lg:text-xl font-semibold text-slate-800 dark:text-slate-200 mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">📈</span> Расчет налога при продаже б/у техники выше остаточной стоимости
                        </h3>
                        <p class="mb-4 sm:mb-5 lg:mb-6 text-slate-600 dark:text-slate-400">
                            Если в результате изменения рыночного спроса, дефицита чипов или роста курса криптовалют вам удалось продать б/у оборудование по
                            цене, которая превышает его текущую остаточную стоимость на балансе, у вас возникает прибыль от реализации.
                        </p>

                        <div class="bg-blue-50 dark:bg-blue-950/20 border border-blue-200 dark:border-blue-900/50 p-2 sm:p-4 rounded-xl mb-4">
                            <h4 class="font-bold text-blue-950 dark:text-blue-300 text-sm sm:text-base lg:text-lg mb-2">
                                <span class="inline mr-1 sm:mr-2">📋</span> Пример продажи оборудования с прибылью:
                            </h4>
                            <p class="mb-4 text-xs md:text-sm text-slate-600 dark:text-slate-400">
                                Организация приобрела ASIC за <strong class="underline">100 000 ₽</strong>. За 2 года эксплуатации на балансе была легально
                                начислена амортизация в размере <strong class="underline">40 000 ₽</strong>. Оборудование продано стороннему лицу за <strong
                                    class="underline">80 000 ₽</strong>.
                            </p>
                            <div
                                class="text-xs space-y-1 bg-white/40 dark:bg-slate-900/40 p-2 sm:p-4 rounded-xl border border-blue-100 dark:border-blue-900 font-mono text-slate-800 dark:text-slate-200">
                                <p>1. Расчет остаточной стоимости устройства: 100 000 ₽ − 40 000 ₽ = 60 000 ₽</p>
                                <p>2. Определение налоговой базы при реализации: 80 000 ₽ (Цена продажи) − 60 000 ₽ (Остаточная цена) = 20 000 ₽</p>
                                <p>3. Налог на прибыль к уплате (ставка 25%): 20 000 ₽ × 25% = <span class="text-sm font-bold text-green-500">5 000 ₽</span>
                                </p>
                            </div>
                        </div>
                        <p class="text-xs text-rose-500">
                            *Внимание: Если оборудование продается после полного нормативного списания (через 3–5 лет), его остаточная стоимость равна 0 ₽. В
                            этом случае вся сумма, полученная от продажи б/у техники, признается чистой прибылью и облагается налогом по стандартной ставке
                            ОСНО. Утверждение, что списанная техника продается без налогов - грубейшая ошибка.
                        </p>
                    </div>

                    <div>
                        <h3 class="text-lg lg:text-xl font-semibold text-slate-800 dark:text-slate-200 mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">📉</span> Учет убытка при продаже оборудования ниже балансовой стоимости
                        </h3>
                        <p class="mb-2 text-slate-600 dark:text-slate-400">
                            В майнинге часты ситуации быстрого морального устаревания техники (выход нового поколения асиков), из-за чего б/у оборудование
                            приходится продавать по цене значительно ниже его остаточной стоимости на балансе. В этом случае фиксируется чистый убыток от
                            реализации.
                        </p>
                        <p class="mb-4 sm:mb-5 lg:mb-6 text-slate-600 dark:text-slate-400">
                            На ОСНО такой убыток регулируется <strong class="text-slate-800 dark:text-slate-200">пунктом 3 статьи 268 НК РФ</strong>. Закон
                            запрещает списывать весь полученный убыток в расходы единовременно в месяце совершения сделки.
                        </p>

                        <div
                            class="bg-white/40 dark:bg-slate-900/40 border border-slate-300 dark:border-slate-700 rounded-xl p-2 sm:p-4 shadow-md shadow-logo-color mb-2 sm:mb-4">
                            <span class="font-bold text-slate-800 dark:text-slate-200 block mb-1">⚖️ Порядок признания убытка по правилам ФНС:</span>
