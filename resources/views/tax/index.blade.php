                            <a href="https://www.consultant.ru/document/cons_doc_LAW_28165/08c2f3c592f23af58538e4378ae625a583418fb0/" target="_blank"
                                class="inline text-indigo-500 hover:text-indigo-600 underline">подпунктов 27 и 28 пункта 2 статьи 146 НК РФ</a>,
                            операции по осуществлению майнинга цифровой валюты и операции по её последующей реализации <strong
                                class="text-green-600 dark:text-green-400 font-bold">не признаются объектом налогообложения НДС</strong>. Организация-майнер не
                            начисляет НДС "сверху" при продаже крипты и не выставляет счета-фактуры покупателям.
                        </p>

                        <div class="p-2 sm:p-4 border border-rose-500/30 bg-rose-500/10 rounded-xl mb-2 sm:mb-4">
                            <span class="font-bold text-rose-500 text-sm block mb-1">⚠️ Финансовое последствие: Запрет вычетов</span>
                            <p class="text-xs md:text-sm text-slate-600 dark:text-slate-400">
                                Поскольку конечная операция освобождена от НДС, организация <strong
                                    class="underline font-medium text-slate-800 dark:text-slate-200">теряет право на налоговый вычет</strong> по входящему НДС.
                                Весь НДС, предъявленный вам энергосбытовыми компаниями за электричество, арендодателями или поставщиками оборудования при
                                покупке АСИКов, <strong class="text-rose-500 font-bold">нельзя вернуть из бюджета</strong>. Этот налог в полном объеме
                                включается в стоимость приобретаемых товаров, работ и услуг (<a class="inline text-indigo-500 hover:text-indigo-600 underline"
                                    href="https://www.consultant.ru/document/cons_doc_LAW_28165/7e99d9a14446e1adf556f812066034a9091e0cc4/" target="_blank">п.
                                    2 ст. 170 НК РФ</a>) и списывается в общие расходы, уменьшающие налог на прибыль.
                            </p>
                        </div>

                        <div
                            class="bg-white/40 dark:bg-slate-900/40 border border-slate-300 dark:border-slate-700 rounded-xl p-2 sm:p-4 shadow-md shadow-logo-color">
                            <span class="font-bold text-slate-800 dark:text-slate-200 text-sm sm:text-base lg:text-lg block mb-1">📊 Обязанность ведения
                                раздельного учета (<a href="https://www.consultant.ru/document/cons_doc_LAW_28165/c8ebcedc9ddce9d959d6c520c3b0d602f71e8e12/"
                                    target="_blank" class="inline text-indigo-500 hover:text-indigo-600 underline">п. 4 ст. 149 НК РФ</a>)</span>
                            <p class="text-xs md:text-sm text-slate-600 dark:text-slate-400">
                                Если компания осуществляет исключительно майнинг, весь входящий НДС просто уходит в расходы. Однако, если юрлицо ведет смешанную
                                деятельность - например, майнит криптовалюту (операция без НДС) и параллельно сдает в аренду коммерческую недвижимость или
                                продает компьютерные комплектующие (операции с НДС 20%), она <strong
                                    class="text-slate-800 dark:text-slate-200 font-semibold">обязана вести жесткий раздельный учет</strong> сумм выручки,
                                затрат и сумм входящего НДС. При отсутствии раздельного учета компания лишается права включать входящий НДС в расходы по налогу
                                на прибыль.
                            </p>
                        </div>
                    </div>

                    <div>
                        <h3 class="text-lg lg:text-xl font-semibold text-slate-800 dark:text-slate-200 mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">⚙️</span>Амортизация оборудования (3-я группа, ст. 258 НК РФ): Линейный метод и СПИ
                        </h3>
                        <p class="mb-2 text-slate-600 dark:text-slate-400">
                            Вычислительная техника высокой мощности (промышленные ASIC-майнеры, GPU-фермы, специализированные серверные стойки и блоки
                            питания) стоимостью свыше 100 000 рублей не может быть списана в расходы единовременно в момент покупки. Такое имущество
                            признается амортизируемым (основными средствами) на основании статьи 256 НК РФ.
                        </p>
                        <p class="mb-4 sm:mb-5 lg:mb-6 text-slate-600 dark:text-slate-400">
                            В соответствии со статьей 258 НК РФ и Общероссийским классификатором основных средств, майнинговое оборудование включено в
                            <strong class="text-slate-800 dark:text-slate-200 font-bold">3-ю амортизационную группу</strong>:
                        </p>

                        <ul class="list-none space-y-3 pl-1 my-4 text-slate-600 dark:text-slate-400 text-xs md:text-sm">
                            <li class="flex items-start gap-2">
                                <span class="text-indigo-500 font-bold mt-0.5">✔</span>
                                <div>
                                    <strong class="text-slate-800 dark:text-slate-200">Срок полезного использования (СПИ):</strong>
                                    Устанавливается организацией самостоятельно в законодательных границах от <strong
                                        class="text-slate-800 dark:text-slate-200">3 до 5 лет включительно (от 36 до 60 месяцев)</strong>. Ввиду быстрого
                                    технологического устаревания и экстремальных эксплуатационных нагрузок, в майнинге целесообразно утверждать минимальный
                                    СПИ - 36 месяцев.
                                </div>
                            </li>
                            <li class="flex items-start gap-2">
                                <span class="text-indigo-500 font-bold mt-0.5">✔</span>
                                <div>
                                    <strong class="text-slate-800 dark:text-slate-200">Линейный метод начисления (ст. 259.1 НК РФ):</strong>
                                    Амортизация рассчитывается отдельно по каждому объекту. Начисление производится ежемесячно равными долями, начиная с
                                    1-го числа месяца, следующего за месяцем ввода устройства в эксплуатацию (приказ по форме ОС-1). Ежемесячная норма
                                    амортизации вычисляется по формуле: <code
                                        class="bg-slate-100 dark:bg-slate-950 px-1.5 py-0.5 rounded-lg font-mono font-bold text-slate-800 dark:text-slate-200">K
                                        = (1 / СПИ в месяцах) × 100%</code>.
                                </div>
                            </li>
                        </ul>
                    </div>

                    <div>
                        <h3 class="text-lg lg:text-xl font-semibold text-slate-800 dark:text-slate-200 mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">📉</span>Правила работы с убытками прошлых лет и ограничение в 50% (ст. 283 НК РФ)
                        </h3>
                        <p class="mb-2 text-slate-600 dark:text-slate-400">
                            Высокая волатильность криптовалютного рынка часто приводит к ситуациям, когда по результатам календарного года совокупные
                            расходы организации (амортизация оборудования, затраты на электроэнергию по тарифам ЦОД, фонд оплаты труда) значительно
                            превышают доход, зафиксированный в рамках Этапа №1. В этом случае у компании формируется чистый налоговый убыток.
                        </p>
                        <p class="mb-4 sm:mb-5 lg:mb-6 text-slate-600 dark:text-slate-400">
                            Перенос и списание таких убытков регулируются положениями <strong class="text-slate-800 dark:text-slate-200 font-bold">статьи 283
                                НК РФ</strong> на базе двух императивных условий:
                        </p>

                        <div class="grid grid-cols-1 md:grid-cols-2 gap-2 sm:gap-4 text-slate-600 dark:text-slate-400">
                            <div
                                class="bg-white/40 dark:bg-slate-900/40 border border-slate-300 dark:border-slate-700 rounded-xl p-2 sm:p-4 shadow-md shadow-logo-color">
                                <strong class="text-slate-800 dark:text-slate-200 block mb-2">🕒 Бессрочный перенос</strong>
                                <span>Законодательное ограничение по сроку переноса убытков (ранее составлявшее 10 лет) полностью исключено. Компания имеет
                                    право переносить полученный убыток на любые последующие налоговые периоды до полного его погашения.</span>
                            </div>
                            <div
                                class="bg-white/40 dark:bg-slate-900/40 border border-slate-300 dark:border-slate-700 rounded-xl p-2 sm:p-4 shadow-md shadow-logo-color">
                                <strong class="text-slate-800 dark:text-slate-200 block mb-2">🛑 Ограничение базы в 50%</strong>
                                <span>Налоговая база за текущий отчетный (налоговый) период <strong
                                        class="underline font-semibold text-slate-800 dark:text-slate-200">не может быть уменьшена более чем на 50%</strong> за
                                    счет накопленных убытков прошлых лет. Организация в любом случае обязана рассчитать и уплатить налог на прибыль минимум
                                    с половины фактически заработанного дохода за текущий год.</span>
                            </div>
                        </div>
                    </div>

                    <div>
                        <h3 class="font-bold text-blue-950 dark:text-blue-300 text-base mb-2 sm:mb-3">
                            <span class="inline mr-1 sm:mr-2">📊</span> Сквозной практический пример учета амортизации и убытков для ООО:
                        </h3>

                        <div class="space-y-2 sm:space-y-4 text-xs md:text-sm text-slate-600 dark:text-slate-400">
                            <div class="p-2 sm:p-4 bg-white/40 dark:bg-slate-900/40 border border-blue-100 dark:border-blue-900 rounded-xl shadow-md">
                                <strong class="text-slate-800 dark:text-slate-200 block mb-2">📅 Год №1 (Капитальные затраты и фиксация убытка):</strong>
                                Компанией приобретено и введено в эксплуатацию оборудование на сумму <strong class="text-slate-800 dark:text-slate-200">1 000
                                    000 ₽</strong>. Выбран минимальный срок полезного использования - 36 месяцев.
                                <div class="mt-4 text-slate-600 dark:text-slate-400 space-y-1">
                                    <p>• Сумма ежемесячной амортизации: 1 000 000 ₽ ÷ 36 мес. = 27 777.78 ₽.</p>
                                    <p>• Сумма начисленной амортизации за первый полный год работы: 27 777.78 ₽ × 12 мес. = 333 333.36 ₽.</p>
                                    <p>• Фактические затраты на электроэнергию (с учетом невычитаемого НДС): 200 000 ₽.</p>
                                    <p>• Итого совокупные задокументированные расходы за первый год: 333 333.36 ₽ + 200 000 ₽ = 533 333.36 ₽.</p>
                                    <p>• Совокупная рыночная стоимость намайненной за этот год криптовалюты на дату зачисления: 400 000 ₽.</p>
                                </div>
                                <span class="font-mono text-xs block mt-4 font-bold text-rose-500">
                                    РЕЗУЛЬТАТ ГОДА №1: Налоговая база = 400 000 ₽ − 533 333.36 ₽ = −133 333.36 ₽ (Убыток). Налог на прибыль к уплате = 0 ₽.
                                    Убыток фиксируется в налоговом регистре.
                                </span>
                            </div>

                            <div class="p-2 sm:p-4 bg-white/40 dark:bg-slate-900/40 border border-blue-100 dark:border-blue-900 rounded-xl shadow-md">
                                <strong class="text-slate-800 dark:text-slate-200 block mb-2">📅 Год №2 (Рост рынка и выход на чистую прибыль):</strong>
                                Оборудование продолжает работать. Амортизация за полный второй год составила 333 333.36 ₽. Затраты на электроэнергию
                                составили 200 000 ₽. Итого совокупные расходы за второй год = 533 333.36 ₽. На фоне роста курса стоимость добытой на кошелек
                                криптовалюты составила <strong class="text-slate-800 dark:text-slate-200">1 000 000 ₽</strong>.
                                <p class="font-mono text-xs mt-4 font-bold text-rose-500">
                                    -> Промежуточная прибыль до вычета убытков: 1 000 000 ₽ − 533 333.36 ₽ = 466 666.64 ₽.
                                </p>
                            </div>

                            <div
                                class="p-2 sm:p-4 bg-white dark:bg-slate-950 border border-blue-200 dark:border-blue-900 rounded-xl font-mono text-xs space-y-1 text-slate-800 dark:text-slate-200">
                                <p class="font-sans font-bold text-slate-500">Алгоритм применения ст. 283 НК РФ (Перенос убытка прошлого периода):</p>
                                <p>1. Расчет лимита уменьшения налоговой базы текущего года (максимум 50%):</p>
                                <p class="pl-4">Лимит списания = 466 666.64 ₽ × 50% = 233 333.32 ₽.</p>
                                <p>2. Сравнение накопленного убытка Года №1 с установленным лимитом ФНС:</p>
                                <p class="pl-4">Наш реальный убыток (133 333.36 ₽) меньше лимита (233 333.32 ₽). Мы имеем полное законное право погасить
                                    его за счет прибыли Года №2 в полном объеме.</p>
                                <p>3. Корректировка чистой налоговой базы Года №2:</p>
                                <p class="pl-4">Окончательная база = 466 666.64 ₽ − 133 333.36 ₽ = 333 333.28 ₽.</p>
                                <p>4. Итоговый расчет налога на прибыль по ставке 25%:</p>
                                <p class="pl-4">Налог к уплате = 333 333.28 ₽ × 25% = <span class="text-sm font-bold text-green-500">83
                                        333.32 ₽</span>.</p>
                            </div>
